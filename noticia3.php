                <h2>14 consejos para mejorar fotos hechas con el móvil y las mejores apps para hacerlo</h2>
                    <h5>Hace 13 horas</h5>
                    <p>La fotografía no deja de ser un arte, y como todo arte, la subjetividad hace acto de presencia.
                        No obstante, hay algunos consejos para mejorar tus fotos. Vamos a contarte desde lo más básicos
                        hasta algunos que quizás no conozcas.</p>
                    <p>Limpia la lente: el primer consejo y el más básico, pero no se suele tener en cuenta la cantidad
                        de veces que perdemos calidad en la fotografía por no tener la lente 100% limpia. Lleva una
                        gamuza cuando quieras salir a hacer fotos o, en su defecto, utiliza una prenda suave, como una
                        camiseta.</p>
                    <p>Utiliza luz natural siempre que puedas: la fotografía es luz, y cuando más abundante y natural
                        sea, mejor. Con baja luz el desempeño de todos los teléfonos baja, incluso en gama alta, así que
                        intenta huir de la luz artificial y valerte de la natural, cuando se pueda.</p>
                    <p>Activa el HDR: en algunos teléfonos el modo HDR no se activa por defecto. Tras nuestras pruebas,
                        es bastante recomendable dejarlo activo, sobre todo por el control que hace de las altas luces.
                    </p>
                    <p>El flash no es tu aliado: el flash LED de nuestros teléfonos, aunque ha avanzado en los últimos
                        años, es bastante agresivo y envuelve la fotografía de una luz muy agresiva. Suele ser
                        recomendable, si lo tenemos, activar el modo noche o valernos de otra fuente de luz artificial
                        menos agresiva.
                    </p>
                    <p>Activa la cuadrícula: la cuadrícula no suele venir activa por defecto en los smartphones, pero es
                        esencial para encuadrar la escena. No tienes más que activarla desde el menú de ajustes, y con
                        ella te ahorrarás más de un disgusto por un encuadre mal hecho.</p>
                    <p>No te compliques con el modo manual: a pesar de que el modo manual está pensado para sacarle
                        partido a las fotografías, la exposición y enfoque ya se pueden controlar en modo automático. Si
                        queremos tener una instantánea genial en pocos segundos (la gracia de usar un móvil), no te
                        compliques y gestiona la exposición desde el modo automático.</p>
                    <p>Desactiva el modo IA de tu teléfono: la IA ha venido a ayudarnos a reconocer escenas y aplicar
                        valores predeterminados en las mismas. Por desgracia, en buena parte de los casos tan solo
                        obtenemos una gran sobresaturación, así que recomendamos desactivar la IA para obtener la
                        fotografía más natural que nos pueda dar el sensor. Si queremos vitaminarla, siempre podemos
                        editarla posteriormente.</p>
                    <p>El sensor principal es tu gran aliado: a pesar de que los móviles tienen múltiples cámaras, el
                        sensor principal sigue siendo el mejor. Salvo contadas excepciones (sobre todo en gama alta), la
                        pérdida de calidad con los sensores ultra gran angular o macro es bastante notable, así que el
                        sensor principal se puede convertir en un gran aliado. Con una sola cámara puedes hacer mucho
                        más de lo que piensas.</p>
                    <p>Haz muchas, muchas fotos: en caso de servidor, por cada sesión que hago con el móvil o la DSLR
                        puedo hacer más de 200 fotos y quedarme, a lo sumo, con un 10% de ellas. Haz fotos, muchas
                        fotos, es la única manera de saber cuál te gusta realmente, de experimentar todas las
                        posibilidades de la escena y, lo más importante, de divertirte y pasarlo bien haciendo fotos.
                    </p>
                    <p>Si quieres retocar un poco, potencia lo básico: cualquier editor de fotos, incluido el nativo de
                        tu teléfono, te dejará tocar valores básicos como la exposición, el contrate o el detalle. La
                        clave en una edición sencilla es no colarse aplicando estos valores de forma muy agresiva. Suele
                        bastar con tocar un poco cada parámetro para tener una imagen mejor que la que hemos tomado.</p>
                        <p>Ajusta el encuadre: si la fotografía no está perfectamente cuadrada, a la hora de editarla
                            tenemos la oportunidad de redimir nuestro error. Ten en cuenta que un reencuadre suele venir
                            de la mano de un pequeño recorte en la fotografía, por lo que podemos perder algo de
                            calidad. No obstante, si el reencuadre es ligero no notaremos dicha pérdida.</p>
                        <p>Usa filtros predeterminados o creados por ti: los filtros pueden mejorar tu fotografía o
                            arruinarla por completo. Aquí no podemos decirte lo que está bien y lo que está mal, ya que
                            es cuestión de gustos. Más adelante hablaremos de apps para editar, con algunos filtros que
                            suelen funcionar siempre. También puedes crearlos tú, y aplicarlos dentro de aplicaciones
                            que también te detallaremos.</p>
                        <p>Elimina lo que no te guste de la fotografía: si alguien o algo se ha colado en la foto, suele
                            ser subsanable en edición. En este artículo te contaremos cómo puedes eliminar objetos de
                            tus fotos, así que hazte a la idea de que esto es posible.</p>
                        <p>Intenta no romper la calidad de la foto: aunque suene extraño, darle demasiada nitidez,
                            exposición o valores básicos a la fotografía la puede destrozar por completo. Fíjate siempre
                            en la calidad original de la foto, e intenta que tu edición no genere artefactos extraños ni
                            acabe con lo que ha logrado captar el sensor de tu teléfono.</p>
